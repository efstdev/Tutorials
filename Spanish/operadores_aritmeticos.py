# El modulo % nos da el residuo de una division
print(10 % 4) # = 2
# El residuo de 10 divido por 4, es 2 por que el 4 solo cabe 2 veces
# Dejandonos con 2

# El simbolo // nos da el entero de una division
print(13 // 4) # = 3
# La division de 13/4 nos da a 3.25, y el entero es 3

# El simbolo / nos da una division en decimales
print(13/4) # = 3.25
# La divison de de 13 / 4, el 4 cabe exactamente 3.25 veces

# EL simbolo de * es una multiplicacion
print(2 * 5) # = 10

# El simbolo ** nos indica un exponente
print(5**2) # = 25
# Un exponente es ese numero multiplicado por si mismo la tantas veces indicadas
# por el exponente, aqui tenemos 5 x 5 que nos da 25

# Los simbolos + y - indican suma y resta
print(1 + 2) # = 3
print( 3 - 2) # = 1